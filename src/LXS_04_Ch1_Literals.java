public class LXS_04_Ch1_Literals {
	public static void main(String[] args) {
		byte age = 19; // by default integer.
		short age2 = 19; // by default integer.
		int age3  = 19; // by default integer.
		long age4 = 565656565L; // L/l is mandatory for long.
		char ch = 'L'; // ''  is mandatory for char.
		float f1 = 5.6f; // F/f is mandatory for float.
		double d1 = 6.5d;  // D/d is not mandatory for double.
		boolean a = true; // by default value is false.
		String str = "loremXsayan";
	}
}
